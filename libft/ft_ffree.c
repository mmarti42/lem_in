#include "libft.h"

char	*ft_ffree(char ***s)
{
	int	i;

	i = 0;
	while ((*s)[i])
	{
		free((*s)[i]);
		(*s)[i] = NULL;
		i++;
	}
	*s = NULL;
	return (NULL);
}
