#include "libft.h"

int		ft_cntword(const char *s, char c)
{
	int		i;
	int		j;

	if (s)
	{
		i = 0;
		j = 0;
		while (s[i] != '\0')
		{
			if (s[i] != c)
			{
				j++;
				while (s[i] != c && s[i] != '\0')
					i++;
				if (s[i] == '\0')
					return (j);
			}
			i++;
		}
		return (j);
	}
	return (0);
}
